interface DropdownMenuItemProps
  extends React.ComponentPropsWithoutRef<typeof DropdownMenuPrimitive.Item> {
  label: string;
  description?: string;
  leadingContent?: React.ReactNode;
  trailingContent?: React.ReactNode;
  verticalPadding?: 'small' | 'medium' | 'large';
  verticalAlign?: 'top' | 'center' | 'bottom';
  disabled?: boolean;
  active?: boolean;
}

const DropdownMenuItem = React.forwardRef<
  React.ElementRef<typeof DropdownMenuPrimitive.Item>,
  DropdownMenuItemProps
>(
  (
    {
      className,
      label,
      description,
      leadingContent,
      trailingContent,
      verticalPadding = 'medium',
      verticalAlign = 'center',
      disabled,
      active,
      ...props
    },
    ref
  ) => {
    const paddingMap = {
      small: 'py-2',
      medium: 'py-3',
      large: 'py-4',
    };

    const alignMap = {
      top: 'items-start',
      center: 'items-center',
      bottom: 'items-end',
    };

    return (
      <DropdownMenuPrimitive.Item
        ref={ref}
        disabled={disabled}
        className={cn(
          'relative flex cursor-default font-semibold select-none gap-2 rounded-xl px-3 text-body-1 tracking-[0.57%] outline-none transition-colors',
          'py-3',
          alignMap[verticalAlign],
          paddingMap[verticalPadding],
          'focus:bg-neutral-900/[0.0375] data-[disabled]:pointer-events-none data-[disabled]:opacity-50',
          active && 'bg-neutral-900/[0.0375]',
          className
        )}
        {...props}
      >
        {leadingContent && (
          <span className='flex shrink-0 items-center justify-center'>
            {leadingContent}
          </span>
        )}
        <div className='flex flex-col min-w-0'>
          <span className='truncate'>{label}</span>
          {description && (
            <span className='text-caption-1 text-neutral-500 truncate'>
              {description}
            </span>
          )}
        </div>
        {trailingContent && (
          <span className='ml-auto flex shrink-0 items-center justify-center'>
            {trailingContent}
          </span>
        )}
      </DropdownMenuPrimitive.Item>
    );
  }
);

DropdownMenuItem.displayName = DropdownMenuPrimitive.Item.displayName;

